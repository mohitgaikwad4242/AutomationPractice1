package AssignmentArray21;
//Print The Number Of Element Present  In Array

public class PrintElementArray {

	public static void main(String[] args) {
		int[] arr = new int[] { 25, 11, 7, 75, 56 };//declared & initialized the array
		//print the elements by using For Each loop
		for (int n : arr) {
			System.out.println(n);
		}
	}
}
